    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        int selectedRow = employee_table.getSelectedRow();
        if(selectedRow == -1){
            if(model.getRowCount() == 0){
                msg_field.setText("Çalışanlar tablosu boş");
            }else{
                msg_field.setText("Güncellemek için bir çalışan seçin.");
            }
        }else{
            int id = (int) model.getValueAt(selectedRow, 0);
            String name = name_field.getText();
            String surname = surname_field.getText();
            String dept = dept_field.getText();
            String salary =salary_field.getText();
            System.out.println(id);
            if(emp.updateEmployeeInfo(id, name, surname, dept, salary)){
                msg_field.setText("Çalışan Bilgileri Güncellendi.");
                showEmployee();
                cleanTextField();
            }else{
                msg_field.setText("Bir hata meydana geldi.");
            }
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        msg_field.setText("");
        
        int selectedRow = employee_table.getSelectedRow();
        
        if(selectedRow == -1){
            if(employee_table.getRowCount() == 0){
                msg_field.setText("Çalışan tablosu boş");
            }else{
                msg_field.setText("Silmek için çalışan seçin.");
            }
        }else{
            int id = (int) model.getValueAt(selectedRow, 0);
            if(emp.deleteEmployee(id)){
                msg_field.setText("Çalışan silindi.");
                showEmployee();
                cleanTextField();
            }else{
                msg_field.setText("Bir hata meydana geldi.");
            }
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed
    
    public void cleanTextField(){
        name_field.setText("");
        surname_field.setText("");
        dept_field.setText("");
        salary_field.setText("");
    }
    public void searchEmployee(String text){
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        employee_table.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(text));
    }
    
