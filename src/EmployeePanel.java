    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        // TODO add your handling code here:
        String name = name_field.getText();
        String surname = surname_field.getText();
        String dept = dept_field.getText();
        int salary =new Integer(salary_field.getText());
        
        if(emp.addNewEmployee(name, surname, dept, salary)){
            msg_field.setText("Yeni Çalışan Başarıyla Eklendi.");
            showEmployee();
        }else{
            msg_field.setText("Bir Hata Meydana Geldi.");
        }
    }//GEN-LAST:event_add_buttonActionPerformed

